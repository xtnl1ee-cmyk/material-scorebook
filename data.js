const groupVarietyCategory = "团综类";
const groupVarietyItems = [];

function makeDateRange(start, end, count) {
  if (count <= 1) return [start];
  const startTime = Date.parse(start);
  const endTime = Date.parse(end || start);
  const step = (endTime - startTime) / (count - 1);
  return Array.from({ length: count }, (_, index) => {
    const date = new Date(startTime + step * index);
    return date.toISOString().slice(0, 10);
  });
}

function addGroupSeries({ id, series, start, end, note, tags = [], episodes }) {
  const dates = makeDateRange(start, end || start, episodes.length);
  episodes.forEach((item, index) => {
    const episode = item.episode || `EP${String(index + 1).padStart(2, "0")}`;
    groupVarietyItems.push({
      id: `${id}-${String(index + 1).padStart(2, "0")}`,
      category: groupVarietyCategory,
      series,
      title: item.title,
      episode,
      date: item.date || dates[index],
      tags: [series, ...tags, ...(item.tags || []), note].filter(Boolean)
    });
  });
}

function placeholderEpisodes(count) {
  return Array.from({ length: count }, (_, index) => ({
    episode: `EP${String(index + 1).padStart(2, "0")}`,
    title: "标题待补充"
  }));
}

